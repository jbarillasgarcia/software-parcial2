package gt.edu.umg.ingenieria.sistemas.software.parcial2.entity;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author rodrigo
 */
@Entity
@Table(name = "t2_course")
@NamedQueries({
    @NamedQuery(name = "T2Course.findAll", query = "SELECT t FROM T2Course t")})
public class T2Course implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id_course")
    private Integer idCourse;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 50)
    @Column(name = "name")
    private String name;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 1000)
    @Column(name = "description")
    private String description;
    @ManyToMany(mappedBy = "t2CourseList")
    private List<T2Student> t2StudentList;

    public T2Course() {
    }

    public T2Course(Integer idCourse) {
        this.idCourse = idCourse;
    }

    public T2Course(Integer idCourse, String name, String description) {
        this.idCourse = idCourse;
        this.name = name;
        this.description = description;
    }

    public Integer getIdCourse() {
        return idCourse;
    }

    public void setIdCourse(Integer idCourse) {
        this.idCourse = idCourse;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public List<T2Student> getT2StudentList() {
        return t2StudentList;
    }

    public void setT2StudentList(List<T2Student> t2StudentList) {
        this.t2StudentList = t2StudentList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (idCourse != null ? idCourse.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof T2Course)) {
            return false;
        }
        T2Course other = (T2Course) object;
        if ((this.idCourse == null && other.idCourse != null) || (this.idCourse != null && !this.idCourse.equals(other.idCourse))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "gt.edu.umg.ingenieria.sistemas.software.parcial2.entity.T2Course[ idCourse=" + idCourse + " ]";
    }
    
}